function SocialLink(props) {
  const iconList = [
    { component: Github, url: "https://github.com/Tsu8sa/" },
    {
      component: Weixin,
      func() {
        showModal("WX");
      },
    },
    { component: Twitter, url: "https://twitter.com/qiulin2483316" },
    { component: Weibo, url: "https://weibo.com/6284276137" },
    { component: Bilibili, url: "https://space.bilibili.com/3273496" },
    {
      component: QQ,
      func() {
        showModal("QQ");
      },
    },
  ];
  const svgOption = {
    width: 20,
    height: 20,
  };
  function gotoLink(url): any {
    if (!url) {
      return;
    }

    const w = window.open("about:blank");
    if (w) {
      w.location.href = url;
    }
  }
  const [isModalVisibleWX, setIsModalVisibleWX] = useState(false);
  const [isModalVisibleQQ, setIsModalVisibleQQ] = useState(false);

  const handleOk = (type) => {
    setIsModalVisibleQQ(false);

    switch (type) {
      case "WX":
        setIsModalVisibleWX(false);
        break;
      case "QQ":
        setIsModalVisibleQQ(false);
        break;
      default:
        break;
    }
  };

  const handleCancel = (type) => {
    switch (type) {
      case "WX":
        setIsModalVisibleWX(false);
        break;
      case "QQ":
        setIsModalVisibleQQ(false);
        break;
      default:
        break;
    }
  };

  const showModal = (type) => {
    switch (type) {
      case "WX":
        setIsModalVisibleWX(true);
        break;
      case "QQ":
        setIsModalVisibleQQ(true);
        break;
      default:
        break;
    }
  };

  return (
    <div className="icon-container">
      {iconList.map((item, index) => {
        return (
          <div
            key={index}
            onClick={() => {
              item.func ? item.func() : gotoLink(item.url);
            }}
          >
            {item.component({
              svgOption,
              index,
            })}
          </div>
        );
      })}

      <Modal
        title="微信 | WeChat"
        onOk={() => {
          handleOk("WX");
        }}
        onCancel={() => {
          handleCancel("WX");
        }}
        mask={false}
        cancelText="取消"
        visible={isModalVisibleWX}
      ></Modal>
      <Modal
        title="企鹅 | QQ"
        onOk={() => {
          handleOk("QQ");
        }}
        onCancel={() => {
          handleCancel("QQ");
        }}
        mask={false}
        visible={isModalVisibleQQ}
        cancelText="取消"
      >
        <img
          style={{ width: "50%", margin: "0 auto" }}
          src="https://belos.xyz/image/qq.png"
        ></img>
      </Modal>
    </div>
  );
}
